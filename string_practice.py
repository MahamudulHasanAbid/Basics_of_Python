# Slicing
a = "hey I am back on python and want to be a python backend engineer"
print(a)
print(a.upper())
print(a.lower())
print(a.center(100))
print(a.rjust(100))
print(a.split(" a "))
print(a.find("b"))
print(a.count("a"))
print(a.capitalize())
print(a.casefold())
print(a.encode())
str = "I was saying:  {}"
print(str.format(a))
print(a.strip())